package cn.batchfile.stat.domain;

public class ResAlloc {
	private float cpus;
	private float mem;
	private float disk;

	public float getCpus() {
		return cpus;
	}

	public void setCpus(float cpus) {
		this.cpus = cpus;
	}

	public float getMem() {
		return mem;
	}

	public void setMem(float mem) {
		this.mem = mem;
	}

	public float getDisk() {
		return disk;
	}

	public void setDisk(float disk) {
		this.disk = disk;
	}
}
